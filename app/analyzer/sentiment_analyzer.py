        "어닝서프라이즈", "서프라이즈", "실적상회", "예상상회", "깜짝실적",

        # 주가 관련
        "급등", "상승", "주가상승", "강세", "52주신고", "신고가", "반등",
        "회복", "상향", "매수", "매수추천", "목표가상향", "목표가올려",
        "주가목표", "긍정적", "상향조정", "낙관",

        # 성장 관련
        "성장", "고성장", "확장", "수주", "계약체결", "계약성사", "수주확대",
